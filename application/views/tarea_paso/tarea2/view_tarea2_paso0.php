<div>
    <h2>2. Especificar la evaluación</h2>
</div>
<hr>
<div>
    <p class="texto_justificado">En esta actividad se especifican las subcaracterísticas a evaluar del producto de software y los criterios de decisión que se aplicarán, tanto en las subcaracterísticas como en la evaluación.</p>
    <p class="texto_justificado">
        <strong>Tarea 1: Seleccionar las subcaracterísticas a evaluar</strong><br>
        En esta tarea el evaluador selecciona las subcaracterísticas sobre las cuales quiere realizar la medición. En base a los resultados obtenidos y a los criterios establecidos, se podrán realizar comparaciones que permitan tomar decisiones futuras.
    </p>
    <p class="texto_justificado">
        <strong>Tarea 2: Definir los criterios de decisión para las subcaracterísticas</strong><br>
        Se deben definir los criterios de decisión para las subcaracterísticas seleccionadas. Dichos criterios son umbrales numéricos que se pueden relacionar con los requisitos de calidad y posteriormente con los criterios de evaluación para decidir la calidad del producto. Estos umbrales se pueden establecer a partir de benchmarks, límites de control estadísticos, datos históricos, requisitos del cliente, etc.
    </p>
    <p class="texto_justificado">
        <strong>Tarea 3: Definir los criterios de decisión de la evaluación</strong><br>
        Se deben definir criterios para las diferentes características evaluadas a partir de las subcaracterísticas. Estos resultados a mayor nivel de abstracción permiten realizar la valoración de la calidad del producto software por cada característica.
    </p>
</div>