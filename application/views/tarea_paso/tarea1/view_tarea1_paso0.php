<div>
    <h2>1. Establecer los requisitos de la evaluación</h2>
</div>
<hr>
<div>
    <p class="texto_justificado">El primer paso del proceso de evaluación consiste en establecer los requisitos de la evaluación.</p>
    <p class="texto_justificado">
        <strong>Tarea 1: Establecer el propósito de la evaluación</strong><br>
        En esta tarea se documenta el propósito por el cual la organización quiere evaluar la calidad de su producto de software (asegurar la calidad del producto, decidir si se acepta un producto, determinar la viabilidad del proyecto en desarrollo, comparar la calidad del producto con productos de la competencia, etc)
    </p>
    <p class="texto_justificado">
    <strong>Tarea 2: Seleccionar las características a evaluar del producto de software</strong><br>
        Se deben especificar la/s característica/s a evaluar del producto, utilizando el modelo de calidad brindado por la norma ISO/IEC 25010.<br>
    </p>
    <p class="texto_justificado">
        <strong>Tarea 3: Indicar la fase en la que se encuentra el producto a evaluar</strong><br>
        Se debe identificar la fase en la que se encuentra el producto de software en el momento de la evaluación. El tipo de producto a evaluar depende de la fase en el ciclo de vida en que se realiza la evaluación y del propósito de ésta.
    </p>
    <p class="texto_justificado">
        <strong>Tarea 4: Definir el rigor de la evaluación</strong><br>
        Se debe definir el rigor de la evaluación en función del propósito y el uso previsto del producto de software, basándose en aspectos como el riesgo para la seguridad física, el riesgo económico y el riesgo para la seguridad de acceso. En función del rigor se podrá establecer qué técnicas se aplican y qué resultados se esperan de la evaluación.
    </p>
</div>
