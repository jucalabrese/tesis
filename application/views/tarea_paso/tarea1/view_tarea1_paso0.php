    <h2>HolaprobandoActividad 1: Establecer los requisitos de la evaluación</h2>
</div>
<hr>
<div>
    <p class="texto_justificado">El primer paso del proceso de evaluación consiste en establecer los requisitos de la evaluación</p>
    <p class="texto_justificado">
        <strong>Tarea 1.1: Establecer el propósito de la evaluación</strong><br>
        En esta tarea se documenta el propósito por el que la organización quiere evaluar la calidad de su producto software (asegurar la calidad del producto, decidir si se acepta un producto, determinar la viabilidad del proyecto en desarrollo, comparar la calidad del producto con productos de la competencia, etc.).<br>
        <strong>Tarea 1.2: Obtener los requisitos de calidad del producto</strong><br>
        En esta tarea se identifican las partes interesadas en el producto software (desarrolladores, posibles adquirientes, usuarios, proveedores, etc.) y se especifican los requisitos de calidad del producto utilizando un determinado modelo de calidad.<br>
        <strong>Tarea 1.3: Identificar las partes del producto que se deben evaluar</strong><br>
        Se deben identificar y documentar las partes del producto software incluidas en la evaluación. El tipo de producto a evaluar (especificación de requisitos, diagramas de diseño, documentación de las pruebas, etc.) depende de la fase en el ciclo de vida en que se realiza la evaluación y del propósito de ésta.<br>
        <strong>Tarea 1.4: Definir el rigor de la evaluación</strong><br>
        Se debe definir el rigor de la evaluación en función del propósito y el uso previsto del producto software, basándose, por ejemplo, en aspectos como el riesgo para la seguridad, el riesgo económico o el riesgo ambiental. En función del rigor se podrá establecer qué técnicas se aplican y qué resultados se esperan de la evaluación.<br>
        <br>
    </p>
