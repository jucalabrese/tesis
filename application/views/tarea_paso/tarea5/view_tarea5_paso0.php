<div>
    <h2>5. Concluir la evaluación</h2>
</div>
<hr>
<p class="texto_justificado">En esta actividad se concluye la evaluación de la calidad del producto de software, realizando el informe de resultados que se entregará al cliente y revisando con éste los resultados obtenidos.</p>
<p class="texto_justificado">
    <strong>Tarea 1: Crear el informe de evaluación</strong><br>
    Se elabora el informe de evaluación, con los requisitos de la evaluación, los resultados, las limitaciones y restricciones, el personal evaluador, etc.<br>
    <strong>Tarea 2: Revisar la calidad de la evaluación y obtener feedback</strong><br>
    El evaluador revisará los resultados de la evaluación y la validez del proceso de evaluación, de los indicadores y de las métricas aplicadas. El feedback de la revisión debe servir para mejorar el proceso de evaluación de la organización y las técnicas de evaluación utilizadas.<br>
    <strong>Tarea 3: Tratar los datos de la evaluación</strong><br>
    Una vez finalizada la evaluación, el evaluador debe realizar el adecuado tratamiento con los datos y los objetos de la evaluación según lo acordado con el cliente (en caso de ser una tercera parte), devolviéndolos, archivándolos o eliminándolos según corresponda.<br>
</p> 