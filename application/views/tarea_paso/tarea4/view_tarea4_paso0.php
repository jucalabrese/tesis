<div>
    <h2>Actividad 4: Ejecutar la evaluación</h2>
</div>
<hr>
<p class="texto_justificado">En esta actividad se ejecutan las actividades de evaluación obteniendo las métricas de calidad y aplicando los criterios de evaluación</p>
<p class="texto_justificado">
    <strong>Tarea 4.1: Realizar las mediciones</strong><br>
    Se deben realizar las mediciones sobre el producto software y sus componentes para obtener los valores de las métricas seleccionadas e indicadas en el plan de evaluación. Todos los resultados obtenidos deberán ser debidamente registrados.<br>
    <strong>Tarea 4.2: Aplicar los criterios de decisión para las métricas</strong><br>
    Se aplican los criterios de decisión para las métricas seleccionadas sobre los valores obtenidos en la medición del producto.<br>
    <strong>Tarea 4.3: Aplicar los criterios de decisión de la evaluación</strong><br>
    En esta última tarea se deben aplicar los criterios de decisión a nivel de características y subcaracterísticas de calidad, produciendo como resultado la valoración del grado en que el producto software cumple los requisitos de calidad establecidos.<br>
</p> 
